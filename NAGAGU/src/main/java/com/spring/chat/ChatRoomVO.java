package com.spring.chat;

public class ChatRoomVO {
	private int chatroom_num;
	private int chatroom_es_order;
	private int chatroom_customer;
	private int chatroom_workshop;
	
	public int getChatroom_num() {
		return chatroom_num;
	}
	public void setChatroom_num(int chatroom_num) {
		this.chatroom_num = chatroom_num;
	}
	public int getChatroom_es_order() {
		return chatroom_es_order;
	}
	public void setChatroom_es_order(int chatroom_es_order) {
		this.chatroom_es_order = chatroom_es_order;
	}
	public int getChatroom_customer() {
		return chatroom_customer;
	}
	public void setChatroom_customer(int chatroom_customer) {
		this.chatroom_customer = chatroom_customer;
	}
	public int getChatroom_workshop() {
		return chatroom_workshop;
	}
	public void setChatroom_workshop(int chatroom_workshop) {
		this.chatroom_workshop = chatroom_workshop;
	}
}
