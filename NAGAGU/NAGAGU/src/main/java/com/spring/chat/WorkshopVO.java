package com.spring.chat;

public class WorkshopVO {
	private String WORK_MAIL;
	private String WORK_NAME;
	private String WORK_PIC;
	
	public String getWORK_MAIL() {
		return WORK_MAIL;
	}
	public void setWORK_MAIL(String wORK_MAIL) {
		WORK_MAIL = wORK_MAIL;
	}
	public String getWORK_NAME() {
		return WORK_NAME;
	}
	public void setWORK_NAME(String wORK_NAME) {
		WORK_NAME = wORK_NAME;
	}
	public String getWORK_PIC() {
		return WORK_PIC;
	}
	public void setWORK_PIC(String wORK_PIC) {
		WORK_PIC = wORK_PIC;
	}
	

}
